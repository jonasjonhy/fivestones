    private void cmbCrearPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCrearPartidaActionPerformed
        // TODO add your handling code here:
        
        byte x=0;
        try{
        x=1;
        }
        catch(Exception r){
            x=0;
        }
        
        if(this.txtJugador1.getText()==null || this.txtJugador1.getText().equals("")||this.txtJugador2.getText()==null ||
                this.txtJugador2.getText().equals("")){
             JOptionPane.showMessageDialog(rootPane, "Llene todas las casillas de Nombre de jugador","Advertencia",JOptionPane.ERROR_MESSAGE);
            return;
        }

        if(this.boton1==null||this.boton2==null){
            JOptionPane.showMessageDialog(rootPane, "Seleccione los iconos del jugador","Advertencia",JOptionPane.ERROR_MESSAGE);
            return;
        }
           
                   
        FormTablero y= new FormTablero(new Fivestones(this.txtJugador1.getText(), this.txtJugador2.getText(),
                convertirString(this.boton1), convertirString(this.boton2)),x);
        y.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_cmbCrearPartidaActionPerformed

