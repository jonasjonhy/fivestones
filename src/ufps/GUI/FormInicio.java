    public String convertirString(Integer x){
        
        if(x==1)
            return "50";
        if(x==2)
            return "100";
        if(x==3)
            return "200";
        if(x==4)
            return "500";
        if(x==5)
            return "1000";
        
        return null;
        
    }
    
